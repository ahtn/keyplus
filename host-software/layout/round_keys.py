RCON = [ 0x01, 0x02, 0x04, 0x08, 0x10, 0x20, 0x40, 0x80, 0x1b, 0x36 ];

KEY_SIZE = 16
NUM_ROUNDS = 10

def gen_final_round_key(enc_key):
    key = bytearray(enc_key)
    for i in range(NUM_ROUNDS):
        key[0] ^= SBOX[key[13]] ^ RCON[i]
        key[1] ^= SBOX[key[14]]
        key[2] ^= SBOX[key[15]]
        key[3] ^= SBOX[key[12]]

        for i in range(12):
            key[i+4] ^= key[i]

    return key

def hex_str(array):
    return ''.join(['{:02x}'.format(i) for i in array])


